-- =============================================================
-- 구매시스템 DDL - 37. RFP 평가입찰 + 역경매 자동연장
--   RFP 평가입찰: RX_RFX 확장 + RX_EVAL_CRITERIA(비가격항목)·RX_EVALUATOR(평가위원)·RX_EVAL_SCORE(점수)
--   역경매 자동연장: AU_AUCTION 확장(AUTO_EXT_YN/END_DT/EXT_*)
-- =============================================================

-- ----- RFP 평가입찰: RFx 헤더 확장 (한 문장으로 다중컬럼 추가) -----
ALTER TABLE RX_RFX ADD (
  EVAL_YN      CHAR(1 CHAR) DEFAULT 'N',
  PRICE_WEIGHT NUMBER(6,2) DEFAULT 0,
  PRICE_METHOD VARCHAR2(10 CHAR) DEFAULT 'MR'
);
COMMENT ON COLUMN RX_RFX.EVAL_YN IS '평가입찰 여부(Y=비가격+가격 종합평가)';
COMMENT ON COLUMN RX_RFX.PRICE_WEIGHT IS '가격 배점(종합 100 중)';
COMMENT ON COLUMN RX_RFX.PRICE_METHOD IS '가격점수 방식(MR최저가비율/LN선형)';

CREATE TABLE RX_EVAL_CRITERIA (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  RFX_ID      NUMBER NOT NULL,
  LINE_NO     NUMBER NOT NULL,
  CRITERIA_NM VARCHAR2(300 CHAR) NOT NULL,
  WEIGHT      NUMBER(6,2) DEFAULT 0,        -- 비가격 배점
  REMARK      VARCHAR2(500 CHAR),
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_RX_EVAL_CRITERIA_1 UNIQUE (RFX_ID, LINE_NO),
  CONSTRAINT FK_RX_EVAL_CRITERIA_1 FOREIGN KEY (RFX_ID) REFERENCES RX_RFX (ID)
);
COMMENT ON TABLE RX_EVAL_CRITERIA IS 'RFP 비가격 평가항목/배점';

CREATE TABLE RX_EVALUATOR (
  ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  RFX_ID     NUMBER NOT NULL,
  USR_ID     VARCHAR2(18 CHAR) NOT NULL,
  USR_NM     VARCHAR2(100 CHAR),
  REG_ID     VARCHAR2(18 CHAR),
  REG_DT     TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_RX_EVALUATOR_1 UNIQUE (RFX_ID, USR_ID),
  CONSTRAINT FK_RX_EVALUATOR_1 FOREIGN KEY (RFX_ID) REFERENCES RX_RFX (ID)
);
COMMENT ON TABLE RX_EVALUATOR IS 'RFP 평가위원';

CREATE TABLE RX_EVAL_SCORE (
  ID           NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  RFX_ID       NUMBER NOT NULL,
  VD_CD        VARCHAR2(18 CHAR) NOT NULL,
  CRITERIA_ID  NUMBER NOT NULL,
  EVALUATOR_ID NUMBER NOT NULL,
  SCORE        NUMBER(6,2) DEFAULT 0,        -- 0~100
  REG_ID       VARCHAR2(18 CHAR),
  REG_DT       TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_RX_EVAL_SCORE_1 UNIQUE (RFX_ID, VD_CD, CRITERIA_ID, EVALUATOR_ID),
  CONSTRAINT FK_RX_EVAL_SCORE_1 FOREIGN KEY (RFX_ID) REFERENCES RX_RFX (ID)
);
COMMENT ON TABLE RX_EVAL_SCORE IS 'RFP 평가위원별 협력사별 항목점수(0~100)';
CREATE INDEX IX_RX_EVAL_SCORE_1 ON RX_EVAL_SCORE (RFX_ID, VD_CD);

-- ----- 역경매 자동연장: AU_AUCTION 확장 -----
ALTER TABLE AU_AUCTION ADD (
  AUTO_EXT_YN     CHAR(1 CHAR) DEFAULT 'N',
  END_DT          TIMESTAMP(0) WITH LOCAL TIME ZONE,
  EXT_TRIGGER_MIN NUMBER DEFAULT 5,
  EXT_MIN         NUMBER DEFAULT 5,
  MAX_EXT_CNT     NUMBER DEFAULT 3,
  EXT_CNT         NUMBER DEFAULT 0
);
COMMENT ON COLUMN AU_AUCTION.AUTO_EXT_YN IS '자동연장 사용(Y)';
COMMENT ON COLUMN AU_AUCTION.END_DT IS '마감 시각(자동연장 기준)';
COMMENT ON COLUMN AU_AUCTION.EXT_TRIGGER_MIN IS '마감 N분 전 입찰 시 연장발동';
COMMENT ON COLUMN AU_AUCTION.EXT_MIN IS '연장 시간(분)';
COMMENT ON COLUMN AU_AUCTION.MAX_EXT_CNT IS '최대 연장횟수';
COMMENT ON COLUMN AU_AUCTION.EXT_CNT IS '현재 연장횟수';
