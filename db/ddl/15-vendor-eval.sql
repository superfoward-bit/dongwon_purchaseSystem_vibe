-- ---------- VD_EVAL_SHEET (평가시트 템플릿) ----------
CREATE TABLE VD_EVAL_SHEET (
  ID        NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  SHEET_CD  VARCHAR2(18 CHAR) NOT NULL,
  SHEET_NM  VARCHAR2(200 CHAR) NOT NULL,
  EVAL_TYP  VARCHAR2(18 CHAR) DEFAULT 'REGULAR',
  DESCRIPTION VARCHAR2(500 CHAR),
  USE_YN    CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
  DEL_YN    CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  REG_ID    VARCHAR2(18 CHAR),
  REG_DT    TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  MOD_ID    VARCHAR2(18 CHAR),
  MOD_DT    TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_VD_EVAL_SHEET_1 UNIQUE (SHEET_CD)
);
COMMENT ON TABLE VD_EVAL_SHEET IS '협력사 평가시트';
COMMENT ON COLUMN VD_EVAL_SHEET.EVAL_TYP IS '평가유형(REGISTER 등록/REGULAR 정기)';

-- ---------- VD_EVAL_SHEET_ITEM (평가항목) ----------
CREATE TABLE VD_EVAL_SHEET_ITEM (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  SHEET_CD    VARCHAR2(18 CHAR) NOT NULL,
  ITEM_SEQ    NUMBER NOT NULL,
  EVAL_ITEM_NM VARCHAR2(200 CHAR) NOT NULL,
  EVAL_CLS    VARCHAR2(18 CHAR) DEFAULT 'QUANT',
  WEIGHT      NUMBER(10,2) DEFAULT 0,
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_VD_EVAL_SHEET_ITEM_1 UNIQUE (SHEET_CD, ITEM_SEQ)
);
COMMENT ON TABLE VD_EVAL_SHEET_ITEM IS '평가항목';
COMMENT ON COLUMN VD_EVAL_SHEET_ITEM.WEIGHT IS '배점(가중치, 합100)';
COMMENT ON COLUMN VD_EVAL_SHEET_ITEM.EVAL_CLS IS '평가구분(QUANT정량/QUAL정성)';

-- ---------- VD_EVAL_GRADE (평가등급) ----------
CREATE TABLE VD_EVAL_GRADE (
  ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  GRADE_CD   VARCHAR2(18 CHAR) NOT NULL,
  GRADE_NM   VARCHAR2(100 CHAR) NOT NULL,
  MIN_SCORE  NUMBER(10,2) DEFAULT 0,
  MAX_SCORE  NUMBER(10,2) DEFAULT 100,
  ACTION_DESC VARCHAR2(300 CHAR),
  SORT_NO    NUMBER DEFAULT 1,
  USE_YN     CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
  DEL_YN     CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  REG_ID     VARCHAR2(18 CHAR),
  REG_DT     TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  MOD_ID     VARCHAR2(18 CHAR),
  MOD_DT     TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_VD_EVAL_GRADE_1 UNIQUE (GRADE_CD)
);
COMMENT ON TABLE VD_EVAL_GRADE IS '협력사 평가등급';

-- ---------- VD_EVAL (평가 실행 헤더) ----------
CREATE TABLE VD_EVAL (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COMP_CD     VARCHAR2(18 CHAR) NOT NULL,
  EVAL_NO     VARCHAR2(30 CHAR) NOT NULL,
  EVAL_TYP    VARCHAR2(18 CHAR) DEFAULT 'REGULAR',
  SHEET_CD    VARCHAR2(18 CHAR),
  SHEET_NM    VARCHAR2(200 CHAR),
  VD_CD       VARCHAR2(18 CHAR),
  VD_NM       VARCHAR2(200 CHAR),
  EVAL_PERIOD VARCHAR2(20 CHAR),
  EVALUATOR_ID VARCHAR2(18 CHAR),
  TOT_SCORE   NUMBER(10,2) DEFAULT 0,
  GRADE_CD    VARCHAR2(18 CHAR),
  GRADE_NM    VARCHAR2(100 CHAR),
  STS         VARCHAR2(18 CHAR) DEFAULT 'ING' NOT NULL,
  REMARK      VARCHAR2(2000 CHAR),
  USE_YN      CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
  DEL_YN      CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  MOD_ID      VARCHAR2(18 CHAR),
  MOD_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_VD_EVAL_1 UNIQUE (COMP_CD, EVAL_NO)
);
COMMENT ON TABLE VD_EVAL IS '협력사 평가 실행';
COMMENT ON COLUMN VD_EVAL.TOT_SCORE IS '종합점수';
COMMENT ON COLUMN VD_EVAL.STS IS '진행상태(ING평가중/DONE완료/CANCEL)';

-- ---------- VD_EVAL_RESULT (평가 항목별 점수) ----------
CREATE TABLE VD_EVAL_RESULT (
  ID            NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  EVAL_ID       NUMBER NOT NULL,
  SHEET_ITEM_SEQ NUMBER NOT NULL,
  EVAL_ITEM_NM  VARCHAR2(200 CHAR),
  WEIGHT        NUMBER(10,2) DEFAULT 0,
  SCORE         NUMBER(10,2) DEFAULT 0,
  WEIGHTED_SCORE NUMBER(10,2) DEFAULT 0,
  OPINION       VARCHAR2(500 CHAR),
  REG_ID        VARCHAR2(18 CHAR),
  REG_DT        TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_VD_EVAL_RESULT_1 UNIQUE (EVAL_ID, SHEET_ITEM_SEQ),
  CONSTRAINT FK_VD_EVAL_RESULT_1 FOREIGN KEY (EVAL_ID) REFERENCES VD_EVAL (ID)
);
COMMENT ON TABLE VD_EVAL_RESULT IS '평가 항목별 점수';

-- ---------- 협력사에 등급 컬럼 추가 ----------
ALTER TABLE VD_VENDOR ADD (GRADE_CD VARCHAR2(18 CHAR), GRADE_NM VARCHAR2(100 CHAR), LAST_EVAL_YMD DATE);
COMMENT ON COLUMN VD_VENDOR.GRADE_CD IS '최근 평가등급';
