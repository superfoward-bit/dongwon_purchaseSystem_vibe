-- ---------- CM_ROLE_FUNC (역할별 메뉴권한) ----------
CREATE TABLE CM_ROLE_FUNC (
  ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  ROLE_ID    VARCHAR2(30 CHAR) NOT NULL,
  MENU_ID    VARCHAR2(30 CHAR) NOT NULL,
  AUTH_CD    VARCHAR2(18 CHAR) DEFAULT 'READ' NOT NULL, -- READ/SAVE/APPROVAL/MANAGEMENT
  REG_ID     VARCHAR2(18 CHAR),
  REG_DT     TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_CM_ROLE_FUNC_1 UNIQUE (ROLE_ID, MENU_ID)
);
COMMENT ON TABLE CM_ROLE_FUNC IS '역할별 메뉴권한';
COMMENT ON COLUMN CM_ROLE_FUNC.AUTH_CD IS '권한(READ/SAVE/APPROVAL/MANAGEMENT)';

-- ---------- CM_USER_ROLE (사용자별 역할) ----------
CREATE TABLE CM_USER_ROLE (
  ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COMP_CD    VARCHAR2(18 CHAR) NOT NULL,
  USR_ID     VARCHAR2(18 CHAR) NOT NULL,
  ROLE_ID    VARCHAR2(30 CHAR) NOT NULL,
  REG_ID     VARCHAR2(18 CHAR),
  REG_DT     TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_CM_USER_ROLE_1 UNIQUE (COMP_CD, USR_ID, ROLE_ID)
);
COMMENT ON TABLE CM_USER_ROLE IS '사용자별 역할';
