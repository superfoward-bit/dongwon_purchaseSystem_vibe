-- ---------- 재고 현황 (창고·품목·LOT 단위 현재고) ----------
CREATE TABLE ST_STOCK (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COMP_CD     VARCHAR2(18 CHAR) NOT NULL,
  WH_CD       VARCHAR2(18 CHAR) NOT NULL,
  ITEM_CD     VARCHAR2(18 CHAR) NOT NULL,
  ITEM_NM     VARCHAR2(300 CHAR),
  LOT_NO      VARCHAR2(40 CHAR) DEFAULT '*' NOT NULL,
  MFG_YMD     DATE,
  EXP_YMD     DATE,
  UNIT_CD     VARCHAR2(18 CHAR),
  QTY         NUMBER(20,5) DEFAULT 0,        -- 현재고
  LAST_IN_YMD  DATE,
  LAST_OUT_YMD DATE,
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  MOD_ID      VARCHAR2(18 CHAR),
  MOD_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_ST_STOCK_1 UNIQUE (COMP_CD, WH_CD, ITEM_CD, LOT_NO)
);
COMMENT ON TABLE ST_STOCK IS '재고 현황(창고·품목·LOT)';

-- ---------- 재고 수불부 (모든 재고 변동 이력) ----------
CREATE TABLE ST_LEDGER (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COMP_CD     VARCHAR2(18 CHAR) NOT NULL,
  TRANS_TYP   VARCHAR2(18 CHAR) NOT NULL,    -- IN/OUT/ADJ
  WH_CD       VARCHAR2(18 CHAR),
  ITEM_CD     VARCHAR2(18 CHAR),
  ITEM_NM     VARCHAR2(300 CHAR),
  LOT_NO      VARCHAR2(40 CHAR),
  IN_QTY      NUMBER(20,5) DEFAULT 0,
  OUT_QTY     NUMBER(20,5) DEFAULT 0,
  BAL_QTY     NUMBER(20,5) DEFAULT 0,        -- 변동 후 잔량
  UNIT_CD     VARCHAR2(18 CHAR),
  SRC_TYP     VARCHAR2(18 CHAR),             -- GR/IS/SC
  SRC_ID      NUMBER,
  SRC_NO      VARCHAR2(30 CHAR),
  TRANS_YMD   DATE,
  REMARK      VARCHAR2(500 CHAR),
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP
);
COMMENT ON TABLE ST_LEDGER IS '재고 수불부(입출고 이력)';
CREATE INDEX IX_ST_LEDGER_1 ON ST_LEDGER (ITEM_CD, WH_CD, TRANS_YMD);

-- ---------- 출고 ----------
CREATE TABLE ST_ISSUE (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COMP_CD     VARCHAR2(18 CHAR) NOT NULL,
  ISSUE_NO    VARCHAR2(30 CHAR) NOT NULL,
  ISSUE_TYP   VARCHAR2(18 CHAR) DEFAULT 'PROD',
  WH_CD       VARCHAR2(18 CHAR),
  REQ_DEPT    VARCHAR2(100 CHAR),
  ISSUE_YMD   DATE,
  STS         VARCHAR2(18 CHAR) DEFAULT 'TMP' NOT NULL,
  REMARK      VARCHAR2(1000 CHAR),
  USE_YN      CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
  DEL_YN      CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  MOD_ID      VARCHAR2(18 CHAR),
  MOD_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_ST_ISSUE_1 UNIQUE (COMP_CD, ISSUE_NO)
);
COMMENT ON TABLE ST_ISSUE IS '재고 출고';

CREATE TABLE ST_ISSUE_ITEM (
  ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  ISSUE_ID   NUMBER NOT NULL,
  LINE_NO    NUMBER NOT NULL,
  ITEM_CD    VARCHAR2(18 CHAR),
  ITEM_NM    VARCHAR2(300 CHAR),
  LOT_NO     VARCHAR2(40 CHAR),
  UNIT_CD    VARCHAR2(18 CHAR),
  QTY        NUMBER(20,5) DEFAULT 0,
  REMARK     VARCHAR2(500 CHAR),
  REG_ID     VARCHAR2(18 CHAR),
  CONSTRAINT UX_ST_ISSUE_ITEM_1 UNIQUE (ISSUE_ID, LINE_NO),
  CONSTRAINT FK_ST_ISSUE_ITEM_1 FOREIGN KEY (ISSUE_ID) REFERENCES ST_ISSUE (ID)
);
COMMENT ON TABLE ST_ISSUE_ITEM IS '재고 출고 품목';

-- ---------- 재고실사 ----------
CREATE TABLE ST_COUNT (
  ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COMP_CD     VARCHAR2(18 CHAR) NOT NULL,
  COUNT_NO    VARCHAR2(30 CHAR) NOT NULL,
  WH_CD       VARCHAR2(18 CHAR),
  COUNT_YMD   DATE,
  STS         VARCHAR2(18 CHAR) DEFAULT 'TMP' NOT NULL,
  REMARK      VARCHAR2(1000 CHAR),
  USE_YN      CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
  DEL_YN      CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  REG_ID      VARCHAR2(18 CHAR),
  REG_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  MOD_ID      VARCHAR2(18 CHAR),
  MOD_DT      TIMESTAMP(0) WITH LOCAL TIME ZONE DEFAULT SYSTIMESTAMP,
  CONSTRAINT UX_ST_COUNT_1 UNIQUE (COMP_CD, COUNT_NO)
);
COMMENT ON TABLE ST_COUNT IS '재고실사';

CREATE TABLE ST_COUNT_ITEM (
  ID         NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  COUNT_ID   NUMBER NOT NULL,
  LINE_NO    NUMBER NOT NULL,
  ITEM_CD    VARCHAR2(18 CHAR),
  ITEM_NM    VARCHAR2(300 CHAR),
  LOT_NO     VARCHAR2(40 CHAR),
  UNIT_CD    VARCHAR2(18 CHAR),
  BOOK_QTY   NUMBER(20,5) DEFAULT 0,    -- 장부수량
  REAL_QTY   NUMBER(20,5) DEFAULT 0,    -- 실사수량
  DIFF_QTY   NUMBER(20,5) DEFAULT 0,    -- 차이(실사-장부)
  REMARK     VARCHAR2(500 CHAR),
  REG_ID     VARCHAR2(18 CHAR),
  CONSTRAINT UX_ST_COUNT_ITEM_1 UNIQUE (COUNT_ID, LINE_NO),
  CONSTRAINT FK_ST_COUNT_ITEM_1 FOREIGN KEY (COUNT_ID) REFERENCES ST_COUNT (ID)
);
COMMENT ON TABLE ST_COUNT_ITEM IS '재고실사 품목';

